nilai = int(input("Masukkan nilai: "))
if nilai >= 90:
    print("Nilai A")
elif nilai >= 80:
    print("Nilai B+")
elif nilai >= 70:
    print("Nilai B")
elif nilai >= 60:
    print("Nilai C+")
elif nilai >= 50:
    print("Nilai C")
elif nilai >= 40:
    print("Nilai D")
else:
    print("Nilai E")